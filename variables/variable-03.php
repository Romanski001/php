<?php

// conersion du type integrer vers float
$nombreVirgule = (float) 42;

// conersion du type integrer vers float avec la fonction floatval
$nombreVirgule = (floatval) 42;

echo $nombreVirgule . "\n";

// conversion du float vers integrer
$nombreEntier = (int) 3.14

// conversion du type integrer vers float avec la fonction floatval
$nombreEntier = intval (3.14)
echo $nombreEntier . "\n";

// conversion du float vers integrer
$nombreEntier2 = (int) 2.9;
echo $nombreEntier2 . "\n"

// arrondir un float vers un integrer en utilisant la fonction round ()
$nombreEntier3 = round(2.9);
echo $nombreEntier3 . "\n"
